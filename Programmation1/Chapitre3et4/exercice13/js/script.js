var compteur = 0;
for (var i = 1; i < 49; i += 2) {
    if (compteur === 5){
        document.write(" </br>");
    }
    else if (compteur === 10){
        document.write(" </br>");
    }
    else if (compteur === 15){
        document.write(" </br>");
    }
    else if (compteur === 20){
        document.write(" </br>");
    }
    else if (compteur === 25){
        document.write(" </br>");
    }
    document.write(i + " ");
    compteur++;
}







